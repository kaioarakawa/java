package br.edu.ifcvideira.beans;

public class Apartamento {
	private int num_apt;
	private String tipo_apartamento;
	private double valor_apt;

	
	

	public int getNum_apt() {
		return num_apt;
	}
	public void setNum_apt(int num_apt) {
		this.num_apt = num_apt;
	}
	public String getTipo_apartamento() {
		return tipo_apartamento;
	}
	public void setTipo_apartamento(String tipo_apartamento) {
		this.tipo_apartamento = tipo_apartamento;
	}
	public double getValor_apt() {
		return valor_apt;
	}
	public void setValor_apt(double valor_apt) {
		this.valor_apt = valor_apt;
	}
	
		
}
